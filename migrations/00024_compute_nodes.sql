-- Reverse-order teardown. The reverse of the Up section is intentional:
-- node_id is dropped before the table so the FK can't dangle. Installed
-- instances will have to be re-backfilled on a re-up via the same
-- sentinel UUID (regenerated by the operator if the synthetic default-
-- local row was deleted manually). Loud-fail per the 00010/00013
-- posture: down-migrations on shipped platforms require a manual runbook.
alter table instances drop column if exists node_id;
drop table if exists compute_nodes;

